export default class IconUtil {
  public static readonly COLOR_CREDIT = 'rgb(137, 208, 157)';
  public static readonly COLOR_DEBIT = 'rgb(231, 138, 135)';

  static allIcons(): Icon[] {
    return ICONS;
  }

  static randomIcon(): Icon {
    const index = Math.floor(Math.random() * ICONS.length);
    const e = ICONS[index];
    return { icon: e.icon, color: e.color, rgb: e.rgb };
  }

  static getIcon(icon: string): Icon {
    const elements = ICONS.filter((i) => i.icon === icon);
    if (elements.length === 0) {
      throw Error(`Icon "${icon}" not found`);
    }
    return elements[0];
  }
}
